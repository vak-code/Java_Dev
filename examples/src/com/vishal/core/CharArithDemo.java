package com.vishal.core;


//Character data type can be handled like integers demo
public class CharArithDemo {

	public static void main(String[] args)
	{
		char ch = 'X';
		
		System.out.println("The value of ch is : " + ch);
		
		ch++;
		System.out.println("The value of ch++ is : " + ch);
		
		ch = 90;
		System.out.println("The value of ch=90 is : " + ch);
	
	}
	
}
